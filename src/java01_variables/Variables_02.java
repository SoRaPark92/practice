package java01_variables;

public class Variables_02 {

	public static void main ( String[] args) {
		
		// int, double, boolean, char
		//----------------------------------------------
		
		//int타입의 변수 공간 선언(Declaration)
		int data;
		// -> int형 데이터를 저장할 수 있는 메모리 공간(변수)이 생성된다	
	
		//data변수 공간에 값을 대입(Assignment)
		data  = 17;
		// -> data변수에 17이라는 값을 저장한다(우측값을 좌측에 대입)
		
		
		data = 99; //값 대입
		// -> data변수에 저장된 17을 지우고 새롭게 99를 저장한다.
		
		
		//data변수에 저장된 17을 불러와서 콘솔에 출력한다.
		System.out.println( data );
// 		System.out.println( 17 );
		
		
	}
	
}